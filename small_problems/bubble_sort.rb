def bubble_sort!(array)
  n = 1
  loop do
    dup = array.dup
    break if dup == sort_once!(array, n)
    n += 1
  end
  p array
end

def sort_once!(arr, n)
  (arr.length-n).times do |i|
    if arr[i] > arr[i + 1]
       arr[i], arr[i + 1] = arr[i + 1], arr[i]
    end
  end
  arr
end

